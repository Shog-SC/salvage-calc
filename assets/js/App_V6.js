let cmatChart = null;

function updateGraph() {
  const scuRec = Number(scuRecInput.value) || 0;
  if (scuRec <= 0) {
    chartCanvas.style.display = "none";
    graphMessage.textContent = "Saisis une valeur SCU Reclaimer.";
    return;
  }

  const cmatPrice = Number(prixCmatInput.value) || 0;
  const data = [];
  const labels = [];

  for (let i = 0; i <= scuRec; i++) {
    const cmatScu = computeReclaimerCmat(i);
    labels.push(i);
    data.push(computeCmatValue(cmatScu, cmatPrice));
  }

  chartCanvas.style.display = "block";
  graphMessage.textContent = "";

  if (cmatChart) {
    cmatChart.destroy();
  }

  cmatChart = new Chart(chartCanvas.getContext("2d"), {
    type: "line",
    data: {
      labels,
      datasets: [
        {
          label: "CMAT",
          data,
          borderWidth: 2
        }
      ]
    },
    options: {
      responsive: true,
      plugins: {
        legend: { display: false }
      }
    }
  });
}

