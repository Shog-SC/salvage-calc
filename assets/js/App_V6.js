function updateRmcPanel() {
  const lang = currentLang;
  const t = i18n[lang];

  const rmcPrice = Number(prixRmcInput.value) || 0;
  const scuLight = Number(scuLightInput.value) || 0;
  const scuRec   = Number(scuRecInput.value)   || 0;

  // Light ships
  const valRmcLight = computeRmcValue(scuLight, rmcPrice);
  rmcLightValuesEl.textContent =
    t.rmcLightValues ? t.rmcLightValues(scuLight, valRmcLight) :
    `SCU: ${scuLight} → ${valRmcLight} aUEC`;

  rmcLightExtraEl.textContent = "";
  rmcLightDecisionEl.textContent = "";

  // Reclaimer
  rmcRecValuesEl.textContent = t.rmcRecValues();
  rmcRecExtraEl.textContent = "";
  rmcRecDecisionEl.textContent = "";
}

function updateCmatPanel() {
  const lang = currentLang;
  const t = i18n[lang];

  const cmatPrice = Number(prixCmatInput.value) || 0;
  const scuLight = Number(scuLightInput.value) || 0;
  const scuRec   = Number(scuRecInput.value)   || 0;

  // Light ships CMAT = display info only
  cmatLightValuesEl.textContent =
    `${t.lightShipsNoCmatText || "Les vaisseaux légers ne génèrent pas de CMAT."}`;

  cmatLightExtraEl.textContent = "";
  cmatLightDecisionEl.textContent = "";

  // Reclaimer CMAT
  const scuCmat = computeReclaimerCmat(scuRec);
  const valCmat = computeCmatValue(scuCmat, cmatPrice);

  cmatRecValuesEl.textContent =
    t.cmatRecValues ? t.cmatRecValues(scuCmat, valCmat)
                    : `SCU CMAT: ${scuCmat} → ${valCmat} aUEC`;

  // Infos supplémentaires
  const pctCmat = ((scuCmat / (scuRec || 1)) * 100).toFixed(1);
  const pctTotal = ((scuCmat / ((scuLight + scuRec) || 1)) * 100).toFixed(1);

  cmatRecExtraEl.textContent =
    t.cmatRecExtra
      ? t.cmatRecExtra(scuRec, scuCmat, pctCmat, pctTotal)
      : `Conversion: ${pctCmat}% (Reclaimer)`;

  // Décision loop RMC vs CMAT
  const rmcPrice = Number(prixRmcInput.value) || 0;
  const valRmc = computeRmcValue(scuRec, rmcPrice);

  if (valCmat > valRmc) {
    cmatRecDecisionEl.textContent =
      t.loopCmatBetter ? t.loopCmatBetter(valCmat - valRmc) :
      `CMAT meilleur (+${valCmat - valRmc} aUEC)`;
  } else if (valRmc > valCmat) {
    cmatRecDecisionEl.textContent =
      t.loopRmcBetter ? t.loopRmcBetter(valRmc - valCmat) :
      `RMC meilleur (+${valRmc - valCmat} aUEC)`;
  } else {
    cmatRecDecisionEl.textContent = t.loopsEqual || "Même valeur.";
  }
}

