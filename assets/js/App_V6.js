/* ---------------------------------------------------------
   ENVOYER UN RUN AU LEADERBOARD ONLINE
--------------------------------------------------------- */
async function sendRunOnline(run) {
  const lang = currentLang;
  const t = i18n[lang];

  saveRunMessageEl.textContent = "";

  if (!currentUser) {
    saveRunMessageEl.textContent = t.saveRunNoUser;
    return;
  }

  if (!run.scu || run.scu <= 0) {
    saveRunMessageEl.textContent = t.saveRunNoScu;
    return;
  }

  const now = Date.now();
  if (now - lastOnlineRunTs < LEADERBOARD_RUN_COOLDOWN_MS) {
    const wait = Math.ceil(
      (LEADERBOARD_RUN_COOLDOWN_MS - (now - lastOnlineRunTs)) / 1000
    );
    saveRunMessageEl.textContent = `Attends ${wait}s avant d'envoyer un autre run.`;
    return;
  }

  const payload = {
    userId: currentUser.id,
    nickname: currentUser.nickname,
    secret: currentUser.secret,
    ship: run.ship,
    scu: run.scu,
    rmcValue: run.rmcValue,
    cmatValue: run.cmatValue,
    total: run.total,
    ts: run.ts
  };

  try {
    const resp = await fetch(LEADERBOARD_API_BASE + "/run", {
      method: "POST",
      headers: { "content-type": "application/json" },
      body: JSON.stringify(payload)
    });

    if (!resp.ok) throw new Error("HTTP " + resp.status);

    const d = await resp.json();
    saveRunMessageEl.textContent = t.saveRunSuccess;

    lastOnlineRunTs = now;
    localStorage.setItem("salvageCalcLastOnlineRunTs", String(now));

    const users = await fetchOnlineLeaderboard(true);
    renderOnlineLeaderboard(users);
  } catch (err) {
    saveRunMessageEl.textContent =
      "Erreur envoi run : " + (err.message || err);
  }
}

