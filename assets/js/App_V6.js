/* ---------------------------------------------------------
   CONTRUCTION DE LA LISTE “MES RUNS”
--------------------------------------------------------- */

function buildMyRunsList() {
  const lang = currentLang;
  const t = i18n[lang];

  const runs = loadLocalRuns();

  myRunsActiveListEl.innerHTML = "";
  myRunsSoldListEl.innerHTML = "";

  let activeCount = 0;
  let soldCount = 0;

  runs.forEach((run, idx) => {
    const li = document.createElement("li");
    const dateStr = new Date(run.ts).toLocaleString();

    const scuText  = run.scu ? `${run.scu} SCU` : "";
    const rmcText  = run.rmcValue ? `${run.rmcValue} aUEC RMC` : "";
    const cmatText = run.cmatValue ? `${run.cmatValue} aUEC CMAT` : "";
    const profitText = run.total ? `${run.total} aUEC total` : "";

    li.textContent = t.myRunsLine(idx + 1, run.ship, scuText, rmcText, cmatText, profitText, dateStr);

    if (run.sold) {
      soldCount++;
      myRunsSoldListEl.appendChild(li);
    } else {
      activeCount++;
      myRunsActiveListEl.appendChild(li);
    }
  });

  myRunsActiveEmptyEl.style.display = activeCount === 0 ? "block" : "none";
  myRunsSoldEmptyEl.style.display = soldCount === 0 ? "block" : "none";
}

